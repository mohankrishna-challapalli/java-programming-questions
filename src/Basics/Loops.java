package Basics;

public class Loops {
    public static void main(String[] args) {
        int j=1;
        while(j==0){
            System.out.println("Three times");
            j++;
        }
        for(int i=0;i<4;i++){
            System.out.println(i);
        }
        do{
            System.out.println("Three time");
        }while(j==0);

    }
}
